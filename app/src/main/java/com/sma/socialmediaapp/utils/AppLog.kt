package com.sma.socialmediaapp.utils

import android.util.Log

/**
 * Created by jijith on 12/26/17.
 */
class AppLog {

    companion object {
        fun e(tag: String, msg: String) {
            Log.e(tag, msg)
        }


        fun d(tag: String, msg: String) {
            Log.e(tag, msg)
        }
    }
}